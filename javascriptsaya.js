function penjumlahan() {
	var input1 = parseInt(document.getElementById("a").value);
	var input2 = parseInt(document.getElementById("b").value);
	document.getElementById("hasil").innerHTML = input1+input2;}

function pengurangan() {
	var input1 = parseInt(document.getElementById("a").value);
	var input2 = parseInt(document.getElementById("b").value);
	document.getElementById("hasil").innerHTML = input1-input2;}

